# Concatinate (Menggabungkan String) #
nama_depan = "ardhi"
nama_belakang = "wahyudhi"
print(nama_depan)  
print(nama_depan + " wahyudhi") 
print(nama_depan,nama_belakang)
print(nama_depan+nama_belakang)
print(nama_depan+" "+nama_belakang)
print("nama saya \"ardhi\"")
print("baris pertama \n baris kedua") # membuat baris baru
print(nama_depan.capitalize()) # membuat huruf pertama menjadi kapital
print(nama_depan.replace(nama_depan, "yudhi")) # merubah string kata
print(nama_depan.replace("a", "i")) # merubah string huruf

